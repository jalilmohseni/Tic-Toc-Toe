package org.example;

/**
 * The Constants interface defines constant values used in the Tic-Tac-Toe game.
 */
public interface Constants {
    /**
     * Represents an empty space on the board.
     */
    static final char SPACE_CHAR = ' ';

    /**
     * Represents the 'O' mark used by one of the players.
     */
    static final char LETTER_O = 'O';

    /**
     * Represents the 'X' mark used by one of the players.
     */
    static final char LETTER_X = 'X';
}
